select B.CODCOLIGADA,a.IDPRD,  a.IDPRD, a.CODUND, c.CODUNDBASE, b.CODUNDCONTROLE, d.CODUNDBASE 
from TCITMORCAMENTO a
inner join TPRODUTODEF b on a.IDPRD = b.IDPRD --and a.CODCOLIGADA = b.CODCOLIGADA
inner join TUND c on a.CODUND = c.CODUND
inner join TUND d  on d.CODUND = b.CODUNDCONTROLE
--inner join TPRODUTO e on e.IDPRD = b.idprd
where codcotacao in ('2018.000710',
'2018.000773',
'2018.000778',
'2018.000754',
'2018.000768',
'2018.000769')
and c.CODUNDBASE <> d.CODUNDBASE